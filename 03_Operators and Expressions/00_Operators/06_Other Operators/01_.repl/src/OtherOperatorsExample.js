// String Concatenation
console.log("First" + "Second" + 1 + 5); // FirstSecond15, not FirstSecond6

// Member Access
console.log(Math.PI);

// Square Brackets
var names = ["John", "George", "Jim"];
console.log(names[0]);

// Parenthesis
console.log(2/(0+2));

// Conditional Operator
let a = 6; b = 4;
console.log(a > b ? 'a > b' : 'b >= a'); // if a is greater than b, it will print "a > b". Otherwise, "b >= a" is printed

// New Operator
console.log(new Number(4));

// Typeof Operator
let c = 3.14159;
console.log(typeof c);